function generateRandomDescription() {
  const eyeGeneral =
    attributes.eyes.general[
      Math.floor(Math.random() * attributes.eyes.general.length)
    ];
  const eyeColor =
    attributes.eyes.color[
      Math.floor(Math.random() * attributes.eyes.color.length)
    ];
  const eyebrow =
    attributes.eyebrows[Math.floor(Math.random() * attributes.eyebrows.length)];
  const skinGeneral =
    attributes.skin.general[
      Math.floor(Math.random() * attributes.skin.general.length)
    ];
  const skinColor =
    attributes.skin.color[
      Math.floor(Math.random() * attributes.skin.color.length)
    ];
  const faceStructure =
    attributes.face.structure[
      Math.floor(Math.random() * attributes.face.structure.length)
    ];
  const nose =
    attributes.nose[Math.floor(Math.random() * attributes.nose.length)];
  const lips =
    attributes.mouth.lips[
      Math.floor(Math.random() * attributes.mouth.lips.length)
    ];
  const teeth =
    attributes.mouth.teeth[
      Math.floor(Math.random() * attributes.mouth.teeth.length)
    ];
  const facialHair =
    attributes.facialHair[
      Math.floor(Math.random() * attributes.facialHair.length)
    ];
  const hairGeneral =
    attributes.hair.general[
      Math.floor(Math.random() * attributes.hair.general.length)
    ];
  const hairColor =
    attributes.hair.color[
      Math.floor(Math.random() * attributes.hair.color.length)
    ];

  return `The eyes are ${eyeGeneral} and color of ${eyeColor}, with ${eyebrow} eyebrows. The skin is ${skinGeneral} and ${skinColor}. The face has a ${faceStructure} structure. The nose is ${nose}. The lips are ${lips}, and the teeth are ${teeth}. The facial hair is ${facialHair}. The hair is ${hairGeneral} and ${hairColor} color.`;
}

document.getElementById("generateButton").addEventListener("click", () => {
  const description = generateRandomDescription();
  document.getElementById("generatedDescription").textContent = description;
});
